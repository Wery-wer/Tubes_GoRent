    private void setDetailPembayaran(){
        Pembayaran pmb = trs.getPembayaran();
        if(pmb.getStatus_bayar() == true){
            jLabel31.setText(pmb.getId_pembayaran());
            jLabel32.setText(pmb.getTanggal_bayar().toString());
            jLabel34.setText("Lunas");
            jLabel37.setText(pmb.getMetode_bayar());
            jLabel38.setText(Integer.toString(pmb.getJumlah_bayar()));
        } else {
            jLabel31.setText("Belum Lunas");
            jLabel32.setText("Belum Lunas");
            jLabel34.setText("Belum Lunas");
            jLabel37.setText("Belum Lunas");
            jLabel38.setText("Belum Lunas");
        }
    }
    
