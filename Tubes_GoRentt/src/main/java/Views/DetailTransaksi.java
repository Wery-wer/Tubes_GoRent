    private void setDetailKendaraan(){
        Mobil mbl = trs.getMobil();
        Motor mtr = trs.getMotor();
        if (mbl != null) {
            jLabel22.setText(mbl.getId_kendaraan());
            jLabel16.setText(mbl.getMerek());
            jLabel18.setText(Integer.toString(mbl.getHarga_sewa()));
            jLabel20.setText(mbl.getTransmisi());
            jLabel19.setText(Integer.toString(mbl.getKapasitas())+" orang");
            jLabel21.setText("1000");
        } else if(mtr != null) {
            jLabel22.setText(mtr.getId_kendaraan());
            jLabel16.setText(mtr.getMerek());
            jLabel18.setText(Integer.toString(mtr.getHarga_sewa()));
            jLabel20.setText(mtr.getTransmisi());
            jLabel19.setText("2 orang");
            jLabel21.setText("500");
        }
    }
    
